//! The tiny RFC services that became DDoS weapons.
//!
//! Echo, Discard, Daytime, Quote of the Day, Chargen and Time were specified in
//! the early 1980s as debugging aids, back when every host on the network was
//! known and trusted. They are four RFCs of about two pages each, and every one
//! of them is still compiled into equipment shipping today.
//!
//! They are worth dissecting for one reason: the UDP variants are reflectors.
//! A single spoofed datagram to Chargen returns up to 512 bytes to whoever the
//! source address claimed to be (RFC 864 — the request's contents are ignored
//! entirely). Quote of the Day does much the same. An attacker sends small
//! packets from a forged address and the victim receives the amplified flood,
//! from a source that has no idea it is participating.
//!
//! So seeing these on a capture is itself the finding. Nothing legitimate has
//! used them in thirty years, and a host answering on UDP 19 from the public
//! internet is a reflector waiting to be used — usually a printer, a switch
//! management port, or an embedded stack with the services left on by default.
//!
//! One module rather than six, because the services are two lines each and the
//! interesting logic — which direction, and how much came back — is shared.

use std::net::IpAddr;

use crate::models::Protocol;

use super::DissectedResult;

/// Seconds between the Time protocol's 1900 epoch and the Unix epoch
/// (RFC 868 gives this constant explicitly).
const EPOCH_1900_TO_1970: u64 = 2_208_988_800;

/// Largest response Chargen and QOTD are specified to send.
const MAX_RESPONSE: usize = 512;

/// Assemble a result. The service port appearing as *source* means this is the
/// answer, which is the direction carrying the reflected volume.
fn result(
    src_ip: Option<IpAddr>,
    dst_ip: Option<IpAddr>,
    src_port: u16,
    dst_port: u16,
    protocol: Protocol,
    summary: String,
) -> DissectedResult {
    DissectedResult {
        src_addr: src_ip,
        dst_addr: dst_ip,
        src_port: Some(src_port),
        dst_port: Some(dst_port),
        protocol,
        summary,
    }
}

// Written out rather than generated by a macro: `every_protocol_is_produced_by
// _some_dissector` greps the sources for `Protocol::X`, and a macro would hide
// every one of these names from it — which is exactly the drift that test
// exists to catch.

pub fn dissect_echo(
    src_ip: Option<IpAddr>,
    dst_ip: Option<IpAddr>,
    src_port: u16,
    dst_port: u16,
    payload: &[u8],
) -> DissectedResult {
    let summary = describe_echo(src_port == 7, payload);
    result(src_ip, dst_ip, src_port, dst_port, Protocol::Echo, summary)
}

