//! The protocol registry — the single source of truth for every protocol
//! netscope knows about.
//!
//! Before this module existed, adding one protocol meant hand-editing eight
//! files: the enum and its `Display`, the TUI colour table, the display-filter
//! keyword list, two classification lists in `flows`, and the education
//! blurbs. Forgetting one was silent — the protocol simply rendered grey, or
//! was unfilterable, or grouped into the wrong flow.
//!
//! Now each protocol is one row in the [`protocols!`] table below and every
//! one of those consumers is *derived* from it. A row that is missing a field
//! is a compile error, not a runtime surprise.
//!
//! ## Adding a protocol
//!
//! 1. Write the dissector in `dissectors/<name>.rs` and `pub mod` it.
//! 2. Add a row here.
//! 3. Bind it to a port in [`crate::dissectors::bindings`].
//! 4. Write its lesson in [`crate::education::lesson`] (the compiler will tell
//!    you — that match is exhaustive).

use crate::models::{PluginProto, PluginTransport};

/// How a protocol groups into flows. Mirrors [`crate::flows::Transport`],
/// which is derived from this.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum TransportClass {
    Tcp,
    Udp,
    Icmp,
    Arp,
    /// Link-layer and hardware-bus captures with no IP transport.
    Other,
}

/// Whether the display-filter lexer can produce `token` as a single bare word.
/// Names with spaces or slashes (`OPC UA`, `EtherNet/IP`) cannot be typed as a
/// predicate, so those protocols reach the filter through an alias.
pub fn is_lexable_token(token: &str) -> bool {
    !token.is_empty()
        && token
            .chars()
            .all(|c| c.is_ascii_alphanumeric() || matches!(c, '.' | '_' | '-' | '+'))
}

/// Declares every protocol, generating the [`Protocol`] enum and the lookup
/// tables each consumer needs. See the module docs for the field meanings.
macro_rules! protocols {
    ($(
        $variant:ident {
            doc:       $doc:literal,
            display:   $display:literal,
            color:     $color:literal,
            transport: $transport:ident,
            rank:      $rank:literal,
            aliases:   [$($alias:literal),* $(,)?],
            blurb:     $blurb:literal,
        }
    )*) => {
        /// A protocol netscope can put in the protocol column.
        ///
        /// Generated by [`protocols!`]. The two non-unit variants are hand-written:
        /// `Plugin` carries a user plugin's name, `Unknown` the reason we gave up.
        #[derive(Debug, Clone, PartialEq, Eq, Hash)]
        pub enum Protocol {
            $(#[doc = $doc] $variant,)*
            /// A protocol recognised by a user-defined plugin (see [`crate::plugins`]).
            /// Carries the plugin's display name and the transport it rode on, so the
            /// protocol column shows the name and flows still group by transport.
            Plugin(PluginProto),
            Unknown(String),
        }

        impl Protocol {
            /// Every registry protocol, in declaration order. Excludes the
            /// `Plugin` and `Unknown` variants, which carry data.
            pub const ALL: &'static [Protocol] = &[$(Protocol::$variant,)*];

            /// The name shown in the protocol column, e.g. `"HTTP/2"`.
            pub fn display_name(&self) -> &'static str {
                match self {
                    $(Protocol::$variant => $display,)*
                    Protocol::Plugin(_) | Protocol::Unknown(_) => "",
                }
            }

            /// The protocol's colour in the TUI packet list, as `(r, g, b)`.
            pub fn color(&self) -> Option<(u8, u8, u8)> {
                let hex: u32 = match self {
                    $(Protocol::$variant => $color,)*
                    Protocol::Plugin(_) | Protocol::Unknown(_) => return None,
                };
                Some((
                    ((hex >> 16) & 0xFF) as u8,
                    ((hex >> 8) & 0xFF) as u8,
                    (hex & 0xFF) as u8,
                ))
            }

            /// How this protocol groups into flows.
            pub fn transport_class(&self) -> TransportClass {
                match self {
                    $(Protocol::$variant => TransportClass::$transport,)*
                    Protocol::Plugin(p) => match p.transport {
                        PluginTransport::Tcp => TransportClass::Tcp,
                        PluginTransport::Udp => TransportClass::Udp,
                    },
                    Protocol::Unknown(_) => TransportClass::Other,
                }
            }

            /// How specific this protocol is as a label for a whole flow.
            /// A flow shows its highest-ranked packet's protocol, so `WebSocket`
            /// (5) beats the `HTTP` (4) handshake that opened it.
            pub fn rank(&self) -> u8 {
                match self {
                    $(Protocol::$variant => $rank,)*
                    Protocol::Plugin(_) => 3,
                    Protocol::Unknown(_) => 0,
                }
            }

            /// Extra display-filter tokens beyond the lowercased display name —
            /// e.g. `mongo` for MongoDB, `ike` for ISAKMP.
            pub fn aliases(&self) -> &'static [&'static str] {
                match self {
                    $(Protocol::$variant => &[$($alias,)*],)*
                    Protocol::Plugin(_) | Protocol::Unknown(_) => &[],
                }
            }

            /// One plain-language sentence about a packet of this protocol.
            pub fn blurb(&self) -> &'static str {
                match self {
                    $(Protocol::$variant => $blurb,)*
                    Protocol::Plugin(_) | Protocol::Unknown(_) => {
                        "Traffic netscope doesn't decode in detail — shown safely anyway."
                    }
                }
            }

            /// Resolve a bare display-filter token (`dns`, `mongo`, `ike`) to its
            /// protocol. Matches the lowercased display name or any alias.
            pub fn from_filter_token(token: &str) -> Option<Protocol> {
                $(
                    if token.eq_ignore_ascii_case($display) $(|| token == $alias)* {
                        return Some(Protocol::$variant);
                    }
                )*
                None
            }
        }

        /// Every bare word the display-filter lexer accepts as a protocol
        /// predicate. Derived from the table, so a new row is filterable for free.
        ///
        /// Display names the lexer cannot produce as a single word (`OPC UA`,
        /// `EtherNet/IP`) are skipped — those protocols carry an alias instead,
        /// which [`aliases_cover_unlexable_display_names`] enforces.
        pub fn filter_tokens() -> Vec<String> {
            let mut out = Vec::new();
            $(
                let display = $display.to_ascii_lowercase();
                if is_lexable_token(&display) {
                    out.push(display);
                }
                $(out.push($alias.to_string());)*
            )*
            out.sort();
            out.dedup();
            out
        }
    };
}

