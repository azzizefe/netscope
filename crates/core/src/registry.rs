impl std::fmt::Display for Protocol {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Protocol::Plugin(p) => write!(f, "{}", p.name),
            Protocol::Unknown(s) => write!(f, "Unknown({s})"),
            other => write!(f, "{}", other.display_name()),
        }
    }
}

